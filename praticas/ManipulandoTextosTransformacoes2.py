# Manipulando Textos
# Python diferencia minúsculas e maiúsculas

########### Alterando a variável #############
frase2 = '   Aprendendo Python  '
print(frase2)

# strip - Remove todos os espaços inúteis de uma String (tanto no começo, quanto no final na string). Os espçaos do meio são mantidos.
print(frase2.strip())

# rstrip - remove apenas os espaços da direita (right)
print(frase2.rstrip())

#lstrip - remove apenas os espaços da esquerda (left)
print(frase2.lstrip())
