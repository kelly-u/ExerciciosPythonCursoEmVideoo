# Manipulando Textos
# Python diferencia minúsculas e maiúsculas

########### Alterando a variável #############
frase2 = '   Aprendendo Python  '
print(frase2)

# strip - Remove todos os espaços inúteis de uma String (tanto no começo, quanto no final na string). Os espaços do meio são mantidos.
print(frase2.strip()) # Aprendendo Python

# rstrip - remove apenas os espaços da direita (right)
print(frase2.rstrip()) #    Aprendendo Python

#lstrip - remove apenas os espaços da esquerda (left)
print(frase2.lstrip()) # Aprendendo Python  #
