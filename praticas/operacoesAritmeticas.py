# Operadores aritméticos

# Adição +
adicao = 5 + 2
print(f'Adição 5 + 2: {adicao}')

# Subtração -
subtracao = 5 - 2
print(f'Subtração 5 - 2: {subtracao}')

# Multiplicação *
multiplicacao = 5 * 2
print(f'Multiplicação 5 * 2: {multiplicacao}')

# Divisão /
divisao = 5 / 2
print(f'Divisão 5 / 2: {divisao}')

# Potência **
potencia = 5**2
print(f'Potência 5 ** 2: {potencia}')

# Divisão inteira //
divisaoInteira = 5 // 2
print(f'Divisão Inteira 5 // 2: {divisaoInteira}')

# Resto da Divisão %
restoDivisao = 5 % 2
print(f'Resto da divisão 5 % 2: {restoDivisao}')