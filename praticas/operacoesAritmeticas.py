# Operadores aritméticos

# Adição +
adicao = 5 + 2
print(f'Adição 5 + 2: {adicao}')

# Subtração -
subtracao = 5 - 2
print(f'Subtração 5 - 2: {subtracao}')

# Multiplicação *
multiplicacao = 5 * 2
print(f'Multiplicação 5 * 2: {multiplicacao}')

# Divisão /
divisao = 5 / 2
print(f'Divisão 5 / 2: {divisao}')

# Potência **
potencia = 5**2
print(f'Potência 5 ** 2: {potencia}')
print(pow(5,2)) # Função interna de potência (assim, perde a ordem de precedência)

# Divisão inteira //
divisaoInteira = 5 // 2
print(f'Divisão Inteira 5 // 2: {divisaoInteira}')

# Resto da Divisão %
restoDivisao = 5 % 2
print(f'Resto da divisão 5 % 2: {restoDivisao}')

# Raiz quadrada
# Faz a potência de um número por meio (1/2)
raizQuadrada = int(81**(1/2)) # sem esse int, a resposta é float. Não é obrigatório, só organização.
print(f'Raiz quadrada de 81 é: {raizQuadrada}')

# Rais cúbica
# Igual a raiz quadrada, mas a potência é por 1/3
raizCubica = int(127 ** (1/3)) # sem esse int, a resposta é float. Não é obrigatório, só organização.
print(f'A raiz cubica de 127 é: {raizCubica}')